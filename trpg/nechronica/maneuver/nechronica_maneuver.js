function getIndex(value, arr, prop) {
  var v = [];
  for (var i = 0; i < arr.length; i++) {
    if (arr[i][prop].match(value)) {
      v.push(i);
    }
  }
  if (!v.length) {
    v = -1;
  }
  return v;
}

function getIndexKey(value, arr, keys) {
  var v = [];
  for (var i = 0; i < keys.length; i++) {
    for (var j = 0; j < arr.length; j++) {
      if (arr[j][keys[i]].match(value)) {
        v.push(j);
      }
    }
  }
  return v;
}

function getIndexN(value, arr, prop) {
  var v = [];
  for (var i = 0; i < arr.length; i++) {
    if (arr[i][prop] == value) {
      v.push(i);
    }
  }
  return v;
}

function del_duplicate(arr) {
  return Array.from(new Set(arr));
}

